function equ = LagrangeEqu(x)

    x1 = x(1);
    x2 = x(2);
    x3 = x(3);
    x4 = x(4);

    equ(1) = x4 - ...
        (x1^2*(x2 + x3) - x1*(x2^2 + 2*x2*x3 + x3^2) + x2*x3^2 + x2^2*x3 + x1^3 - x2^3 - x3^3) / ...
        (4*x2*x3 + 2*x1^2 + 2*x2^2 + 2*x3^2 + x1*(4*x2 + 4*x3));
    
    equ(2) = x4 + ...
        (x2*x3^2 - x1*(x2^2 - 2*x2*x3 + x3^2) - x2^2*x3 + x1^3 - x2^3 + x3^3 + x1^2*(x2 - x3)) / ...
        (4*x2*x3 + 2*x1^2 + 2*x2^2 + 2*x3^2 + x1*(4*x2 + 4*x3));
    
    equ(3) = x4 - ...
        (x1*(x2^2 - 2*x2*x3 + x3^2) + x2*x3^2 - x2^2*x3 - x1^3 - x2^3 + x3^3 + x1^2*(x2 - x3)) / ...
        (4*x2*x3 + 2*x1^2 + 2*x2^2 + 2*x3^2 + x1*(4*x2 + 4*x3));
    
    equ(4) = x1 + x2 + x3 - 100;

end